package sistemaFipe;

public class Motorista{
    public String nome;
    public String est_civ;

    public Motorista(String nome, String est_civ){
        this.nome = nome;
        this.est_civ = est_civ;
    }

    public String getNome() {
        return nome;
    }
    public String getEst_civ() {
        return est_civ;
    }


}
